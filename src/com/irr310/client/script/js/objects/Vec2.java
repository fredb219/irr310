package com.irr310.client.script.js.objects;

public class Vec2 {

    private final double y;
    private final double x;

    public Vec2(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return x;
    }
    
    public double getY() {
        return y;
    }

}
